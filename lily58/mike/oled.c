enum layer_number {
    _QWERTY = 0,
    _LOWER,
    _RAISE,
    _ADJUST,
};

#define ANIM_FRAME_DURATION 200
#define ANIM_SIZE 96
#define MIN_WALK_SPEED 10
#define MIN_RUN_SPEED 60

static uint32_t anim_timer = 0;
